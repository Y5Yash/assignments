function t = BitPlane(im)
a1  = 255*bitand(im,2^0);
a2  = 255*bitand(im,2^1);
a3  = 255*bitand(im,2^2);
a4  = 255*bitand(im,2^3);
a5  = 255*bitand(im,2^4);
a6  = 255*bitand(im,2^5);
a7  = 255*bitand(im,2^6);
a8  = 255*bitand(im,2^7);
subplot(3,3,1);
imshow(a1);
title('Bit = 1');
subplot(3,3,2);
imshow(a2);
title('Bit = 2');
subplot(3,3,3);
imshow(a3);
title('Bit = 3');
subplot(3,3,4);
imshow(a4);
title('Bit = 4');
subplot(3,3,5);
imshow(a5);
title('Bit = 5');
subplot(3,3,6);
imshow(a6);
title('Bit = 6');
subplot(3,3,7);
imshow(a7);
title('Bit = 7');
subplot(3,3,8);
imshow(a8);
title('Bit = 8');
subplot(3,3,9);
imshow(im);
title('Original Image');